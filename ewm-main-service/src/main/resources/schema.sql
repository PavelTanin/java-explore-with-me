DROP TABLE IF EXISTS participants CASCADE;
DROP TABLE IF EXISTS comp_events CASCADE;
DROP TABLE IF EXISTS compilations CASCADE;
DROP TABLE IF EXISTS events CASCADE;
DROP TABLE IF EXISTS categories CASCADE;
DROP TABLE IF EXISTS locations CASCADE;
DROP TABLE IF EXISTS users CASCADE;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    email VARCHAR NOT NULL,
    name VARCHAR NOT NULL,
    CONSTRAINT user_pk PRIMARY KEY (id),
    CONSTRAINT USER_NAME_UQ UNIQUE(name),
    CONSTRAINT USER_EMAIL_UQ UNIQUE(email)
);

CREATE TABLE IF NOT EXISTS categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR NOT NULL,
    CONSTRAINT category_pk PRIMARY KEY (id),
    CONSTRAINT CATEGORY_NAME_UQ UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS locations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    lat NUMERIC NOT NULL,
    lon NUMERIC NOT NULL,
    CONSTRAINT location_pk PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    title VARCHAR NOT NULL,
    annotation VARCHAR NOT NULL,
    description VARCHAR NOT NULL,
    event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    paid BOOLEAN,
    participant_limit BIGINT,
    request_moderation BOOLEAN,
    initiator BIGINT NOT NULL,
    category BIGINT,
    pinned BOOLEAN,
    location BIGINT,
    state VARCHAR,
    created_on TIMESTAMP WITHOUT TIME ZONE,
    published_on TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT event_pk PRIMARY KEY (id),
    CONSTRAINT EVENT_ON_INITIATOR_FK FOREIGN KEY (initiator) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT EVENT_ON_CATEGORY_FK FOREIGN KEY (category) REFERENCES categories(id),
    CONSTRAINT EVENT_ON_LOCATION_FK FOREIGN KEY (location) REFERENCES locations(id)
);

CREATE TABLE IF NOT EXISTS participants (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    event_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    status VARCHAR NOT NULL,
    CONSTRAINT participant_pk PRIMARY KEY (id),
    CONSTRAINT PARTICIPANT_ON_EVENT_FK FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE,
    CONSTRAINT PARTICIPANT_ON_USER_FK FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS compilations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    title VARCHAR NOT NULL,
    pinned BOOLEAN NOT NULL,
    CONSTRAINT compilations_pk PRIMARY KEY (id),
    CONSTRAINT comp_title_uq UNIQUE (title)
);

CREATE TABLE IF NOT EXISTS comp_events (
    compilation_id BIGINT NOT NULL,
    event_id BIGINT NOT NULL,
    CONSTRAINT COMP_EVENTS_ON_COMPILATION_FK FOREIGN KEY (compilation_id) REFERENCES compilations(id) ON DELETE RESTRICT,
    CONSTRAINT COMP_EVENTS_ON_EVENT_FK FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE
);